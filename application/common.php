function http_curl($url='', $type='get', $post_data = ''){
    $ch = curl_init();
    curl_setopt($ch, CURLOPT_URL, $url);
    curl_setopt($ch, CURLOPT_RETURNTRANSFER, 1);
    $res = curl_exec($ch);
    if(curl_errno($ch)){
        echo 'curl error: '.curl_error($ch)."\n";
    }
    curl_close($ch);
    return $res;
}
